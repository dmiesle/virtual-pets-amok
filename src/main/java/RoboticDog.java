
public class RoboticDog extends Pet {

	public RoboticDog(String petTag, String name, int boredom, int food, int water, int cleanliness, int health) {
		this.petTag = petTag;
		this.setName(name);
		this.boredom = boredom;
		this.food = food;
		this.water = water;
		this.cleanliness = cleanliness;
		this.health = health;
	}

	
}